"""message_guard.contains_contact_info の単体テスト — 電話番号/URL/メールアドレス検知。

入札メッセージ経由の脱プラットフォーム勧誘（利用規約の禁止行為）を検知する
純粋関数のロジックのみを検証する（DB・HTTPは介さない）。
"""

from __future__ import annotations

import pytest

from app.services.message_guard import contains_contact_info


# ──────────────────────────── 検知すべきケース ────────────────────────────


@pytest.mark.parametrize(
    "text",
    [
        # --- 電話番号: 基本形式 ---
        "090-1234-5678",
        "09012345678",
        "０９０１２３４５６７８",
        "090 1234 5678",
        "03-1234-5678",
        "0120-00-1234",
        "ご質問はお電話ください 090-1234-5678 まで",
        # --- 電話番号: 拡張区切り文字（H1対応） ---
        "090,1234,5678",
        "090/1234/5678",
        "090:1234:5678",
        "090・1234・5678",
        "03(1234)5678",
        "090.1234.5678",
        "０９０　１２３４　５６７８",  # 全角スペース区切り
        # --- 電話番号: グループ窓判定（時刻レンジ+実番号の隣接） ---
        "13:00-15:00、0120-00-1234",
        "0120,00,1234",
        # --- 電話番号: 数字前置攻撃（純数字ラン11桁は候補全体判定で検知） ---
        "500円 09012345678",
        # --- 電話番号: ゼロ幅文字によるバイパス対策 ---
        "090​1234​5678",
        # --- 電話番号: +81国際表記 ---
        "+81 90 1234 5678",
        # --- URL/メール: 基本形式 ---
        "https://example.com",
        "http://example.com/x",
        "www.example.com",
        "info@example.com",
        "ｉｎｆｏ＠ｅｘａｍｐｌｅ．ｃｏｍ",
        # --- URL/メール: 大文字（QA指摘対応） ---
        "HTTPS://EXAMPLE.COM",
        "WWW.EXAMPLE.COM",
        "INFO@EXAMPLE.COM",
    ],
)
def test_detects_contact_info(text: str):
    """電話番号・URL・メールアドレスを含むテキストは検知される。"""
    assert contains_contact_info(text) is True


# ──────────────────────────── 検知してはならないケース ────────────────────────────


@pytest.mark.parametrize(
    "text",
    [
        None,
        "",
        "50,000円でお引き受けできます",
        "080点の高評価です",
        "2026年7月17日に伺えます",
        "ゼロキュウゼロイチニサンヨン",
        "012345678901",
        "本日は貴重なご依頼をいただきありがとうございます。丁寧に対応いたします。",
        # --- 境界値: 日付/時刻の羅列（グループ窓4上限で誤結合しないこと） ---
        "07/17 09:00-18:00に伺えます",
        "2026/07/17 09:00",
        "10,000〜50,000円",
        "7/10、7/11、7/12の09:00",
        "090万円",
        # --- 境界値: 桁数 ---
        "1234567890",  # 10桁だが先頭0でない
        "012345678",  # 9桁
        # --- URL/メール: 誤検知防止 ---
        "example.com",  # 裸ドメイン（設計判断として非検知固定）
        "単価@1.5万円で対応します",  # ドメイン側がASCII英字TLDを持たない
        # --- 境界値: 3桁カンマ区切り金額レンジ（Critical回帰固定） ---
        "価格帯は1,000,000-1,200,000円です",
        "概算は800,000-1,000,000円です",
        "お見積りは2,000,000-2,500,000円の想定です",
        "概算は50,000-80,000円です",
        "概算は100,000-150,000円です",
        # --- 境界値: 時刻レンジ+後続4桁の橋渡し誤検知（High回帰固定） ---
        "現地には13:00-15:00 0570以降にお伺いします",
    ],
)
def test_does_not_detect_false_positives(text: str | None):
    """通常の入札文・金額・日付・カナ電話表記・長い連続数字・裸ドメイン等は誤検知しない。"""
    assert contains_contact_info(text) is False
