"""入札メッセージの脱プラットフォーム勧誘検知 — 連絡先/URLの埋め込みを検知する。

業者が入札メッセージ（``BidOut.message`` として選定前のユーザーにも表示される）に
電話番号・URL・メールアドレスを埋め込み、プラットフォーム外への直接連絡を
誘導する行為（利用規約の禁止行為）を作成時に拒否するための純粋関数群。
DB・I/O には一切依存せず、単体テストのみで検証可能な設計とする。
"""

from __future__ import annotations

import re
import unicodedata

# ── ゼロ幅文字（バイパス対策） ──────────────────────────────────
# ゼロ幅スペース(U+200B)・ZWJ・BOM・ソフトハイフン等のUnicodeカテゴリ Cf は、
# 区切り文字ホワイトリストの間に挟み込むことで検知を回避するバイパス
# （例: "090​1234​5678"）に使われ得るため、判定対象文字列から除去する。
_ZERO_WIDTH_CATEGORY = "Cf"

# ── 電話番号（日本形式） ────────────────────────────────────────
# 数字と数字の間に挟まれた区切り文字（ハイフン類・半角/全角スペース・ドット・
# 括弧・読点/カンマ・スラッシュ・コロン・セミコロン・中点・パイプ・アンダー
# スコア・アスタリスク）のみを除去対象とする。全角側（，／：；｜＊／･）は
# NFKC正規化で半角/該当記号に変換済みのため、ここでは半角側のみを列挙する。
# まず「数字または区切り文字」が連続する範囲だけを候補として切り出し、その
# 候補内の数字ラン（区切りで分割された数字の塊）を最大4グループまでの
# 「窓」で連結して電話番号形式か判定する（詳細は _contains_phone_number 参照）。
_PHONE_SEPARATOR_CHARS = r"\-‐‒–—―ー−\s.(),、/:;・|_*"
_PHONE_CANDIDATE_RE = re.compile(rf"[0-9{_PHONE_SEPARATOR_CHARS}]+")
_DIGIT_RUN_RE = re.compile(r"[0-9]+")

# ── 3桁カンマ区切り数値（金額表記）の無害化 ─────────────────────
# カンマを区切り文字に含めたことで「800,000-1,000,000円」等の金額レンジが
# 1候補に融合し、窓判定が無関係な数値の断片を橋渡し連結して電話番号と
# 誤検知するリグレッションが生じたため、候補抽出前に3桁区切りの数値表記
# （例: 800,000 / 1,000,000）を "#"（数字でも区切り文字でもない）に置換し、
# 候補を分断してから電話番号判定を行う。
_THOUSANDS_NUMBER_RE = re.compile(r"(?<![\d,])\d{1,3}(?:,\d{3})+(?!\d)")

# ── URL ────────────────────────────────────────────────────────
# 裸ドメイン（例: example.com 単体）は誤検知リスクが高いため対象外とし、
# スキーム付き（https?://）または www. から始まるドメイン形式のみを検知する。
_URL_RE = re.compile(r"https?://\S+|www\.[a-z0-9][a-z0-9-]*(?:\.[a-z0-9-]+)+")

# ── メールアドレス ──────────────────────────────────────────────
# TLD相当部分をASCII英字2文字以上に限定する。\w は日本語文字も含むため
# ドメイン側を [a-z0-9] ベースに限定し、「単価@1.5万円」等の非メール文字列
# （ドメイン側が数字/日本語で終わりASCII英字のTLDを持たないケース）を
# 誤検知しないようにする。
_EMAIL_RE = re.compile(r"[\w.+-]+@[a-z0-9][a-z0-9-]*(?:\.[a-z0-9-]+)*\.[a-z]{2,}")


def _strip_zero_width_chars(text: str) -> str:
    """Unicodeカテゴリ Cf（ゼロ幅文字）を全除去する。"""
    return "".join(ch for ch in text if unicodedata.category(ch) != _ZERO_WIDTH_CATEGORY)


def _is_phone_digits(digits: str) -> bool:
    """連結済み数字列が日本の電話番号形式（+81国際表記含む）か判定する。

    - 非数字に挟まれた10〜11桁・先頭0（ただし「00」始まりを除く）: 090/080/070
      携帯、03等固定電話、0120/0800/050等のフリーダイヤル・IP電話を包含する。
      日本の電話番号体系には先頭「0」の次が「0」になる番号（00始まり）は
      存在しないため、時刻レンジ等の橋渡し連結（例: "0015000570"）を除外する
      目的でも「00」始まりは除外する。
    - 81始まり12桁: 国際表記 "+81 90 1234 5678" の "+" が候補抽出時に
      非digit/非区切り文字として脱落し「81901234 5678」相当（12桁・81始まり）
      になったケースを救済する。
    """
    if len(digits) in (10, 11) and digits.startswith("0") and not digits.startswith("00"):
        return True
    return len(digits) == 12 and digits.startswith("81")


def _contains_phone_number(normalized_text: str) -> bool:
    """ゼロ幅除去・NFKC正規化済みテキストから日本の電話番号形式を検知する。

    まず3桁カンマ区切りの金額表記（例: 800,000 / 1,000,000）を "#" に
    置換して候補を分断し、金額レンジが1候補に融合して窓判定が誤って
    橋渡し連結するのを防ぐ（詳細はモジュールdocstringの「既知の制限」参照）。

    候補（数字/区切り文字が連続する範囲）ごとに、まず候補全体の数字を
    連結して判定し、続いて連続する最大4グループ（区切りで分割された数字の
    塊）の窓を総当たりで連結して判定する。4グループ上限は「07/17 09:00-18:00」
    のような日付+時刻の羅列が誤って1つの電話番号として検知されるのを防ぐ
    ための意図的な制約（詳細はモジュールdocstringの「既知の制限」参照）。
    """
    text = _THOUSANDS_NUMBER_RE.sub("#", normalized_text)
    for candidate in _PHONE_CANDIDATE_RE.findall(text):
        groups = _DIGIT_RUN_RE.findall(candidate)
        if _is_phone_digits("".join(groups)):
            return True
        for i in range(len(groups)):
            acc = ""
            for j in range(i, min(i + 4, len(groups))):
                acc += groups[j]
                if len(acc) > 12:
                    break
                if _is_phone_digits(acc):
                    return True
    return False


def contains_contact_info(text: str | None) -> bool:
    """テキストに電話番号・URL・メールアドレスが含まれるか判定する。

    入札メッセージ経由の脱プラットフォーム勧誘（利用規約の禁止行為）を
    入札作成時に拒否するためのガードとして使用する。

    検知対象:
        - 日本の電話番号形式（携帯090/080/070、固定03等、0120/0800/050等を
          含む、非数字に挟まれた10〜11桁・先頭0の数字列。および "+81 90 ..."
          のような81始まり12桁の国際表記）。全角数字、およびハイフン類・
          半角/全角スペース・ドット・括弧・読点/カンマ・スラッシュ・コロン・
          セミコロン・中点・パイプ・アンダースコア・アスタリスクによる区切り
          を許容し、ゼロ幅文字（U+200B等）を挟んだバイパスも除去した上で判定する。
        - ``https://`` / ``http://`` から始まるURL、``www.`` から始まるドメイン形式。
        - メールアドレス形式（``local@domain.tld`` 相当。TLDはASCII英字2文字以上）。

    既知の制限（過剰検知回避と実装の単純さを優先した意図的な設計判断）:
        - 「ゼロキュウゼロ」等のカナ/漢数字表記の電話番号は検知対象外。
        - 裸ドメイン（スキーム/``www.`` を伴わない ``example.com`` 単体）は検知対象外。
        - LINE ID等のSNS ID文字列は形式が定まらないため原理的に検知不能
          （本ガードは全ての脱プラットフォーム勧誘を防げるものではなく、
          あくまでベストエフォートの規約違反防止策である）。
        - 先頭0の10〜11桁の数字列であれば見積番号・伝票番号等の電話番号
          以外の数字列も誤検知し得る（過小検知より過剰検知を許容する
          意図的な判断。誤検知時はエラーメッセージで理由を明示する）。
        - 電話番号のグループ窓判定は最大4グループまでのため、5グループ
          以上に細分された表記（例: 区切り文字で1〜2桁ずつ極端に分割された
          番号）は、候補全体の連結が10〜11桁にならない限り検知漏れし得る。
        - 3桁カンマ区切りの数値表記に偽装した電話番号（例: "0,901,234,567,8"）
          は候補抽出前に無害化されるため検知対象外（「800,000-1,000,000円」
          等の金額レンジ表記の誤検知解消を優先した意図的なトレードオフ）。
        - 「00」始まりの数字列は電話番号として扱わない（日本の電話番号体系に
          存在しないため）。

    Args:
        text: 判定対象の文字列（``None``・空文字は非検知として扱う）。

    Returns:
        連絡先やURLが含まれると判断した場合 ``True``。
    """
    if not text:
        return False

    # 全角数字・全角英字・全角＠・全角．等を半角化する。
    normalized = unicodedata.normalize("NFKC", text)
    # ゼロ幅文字挿入によるバイパスを防ぐため除去する（以降の全判定の入力とする）。
    normalized = _strip_zero_width_chars(normalized)
    # URL/メール判定は大文字小文字を無視するため小文字化した文字列で行う。
    lowered = normalized.lower()

    if _contains_phone_number(normalized):
        return True
    if _URL_RE.search(lowered):
        return True
    if _EMAIL_RE.search(lowered):
        return True
    return False
